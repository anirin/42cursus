Animal::Animal() {}

Animal::Animal(const Animal& src) : type_(src.type_) {}

/*
** -------------------------------- DESTRUCTOR --------------------------------
*/

Animal::~Animal() {}

/*
** --------------------------------- OVERLOAD ---------------------------------
*/

Animal& Animal::operator=(Animal const& rhs) {
	if (this != &rhs) {
		this->type_ = rhs.type_;
	}
	return *this;
}

